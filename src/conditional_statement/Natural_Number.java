public class Natural_Number {

	public static void main(String[] args) {
		// Write a program in Java to display the first 10 natural numbers.
		// Natural Number: all the positive integers from 1 till infinity
		
		System.out.println("First ten natural numbers are: ");
		
		for (int i = 1; i <=10; i++){
			System.out.print(i + " ,");
		}
	}

}
